package pack1;

public class SwitchCase {

	public static void main(String[] args) {
		
		int i = 30;
		
		switch(i) {
		
		case 10:{
			System.out.println("10");
			break;
		}
		case 11: {
			System.out.println("11");
			break;
		}
		case 2: {
			System.out.println("2");
			break;
		}
		case 29: {
			System.out.println("29");
			break;
		}
		default: {
			System.out.println("default");
		}
			
		
		}
		System.out.println("end");
	}

}
